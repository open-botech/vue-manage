<script>
import { onMounted, reactive, ref, getCurrentInstance } from "vue";
import publicFn from "../../utils/publicFn";
export default {
  name: "Leave",
  setup() {
    const { proxy } = getCurrentInstance();
    const queryForm = reactive({
      applyState: 0,
    }); //查询功能表单对象
    // 动态表格数据对象
    const leaveData = ref([]);
    //动态表格字段格式
    const columList = reactive([
      {
        prop: "orderNo",
        label: "单号",
      },

      {
        prop: "dataClass",
        label: "感知资源类型",
        formatter(row, col, value) {
          return {
            1: "静态数据",
            2: "动态数据"
          }[value];
        },
      },
      {
        prop: "dataItem",
        label: "感知数据资源项"
      },
      {
        prop: "dataCategory",
        label: "感知资源分类",
        formatter(row, col, value) {
          return {
            1: "人脸",
            2: "卡口",
            3: "结构化",
            4: "社区"
          }[value];
        },
      },
      {
        prop: "dataLevel",
        label: "感知资源分级",
        formatter(row, col, value) {
          return {
            1: "一级",
            2: "二级",
            3: "三级",
            4: "四级"
          }[value];
        },
      },
      {
        prop: "dept",
        label: "申请单位"
      },
      {
        prop: "person",
        label: "申请人"
      },
      {
        prop: "personPhone",
        label: "申请人联系方式"
      },
      {
        prop: "applyTime",
        label: "申请时间"
      },
      {
        prop: "reasons",
        label: "申请原因"
      },

      {
        prop: "auditUsers",
        label: "审批人"
      },
			{
        prop: "curAuditUserName",
        label: "当前审批人"
      },
			{
        prop: "applyState",
        label: "审批状态",
        formatter(row, col, value) {
          return {
            1: "待审批",
            2: "审批中",
            3: "审批拒绝",
            4: "审批通过",
            5: "作废"
          }[value];
        }
      }
    ]);
    // 分页数据对象
    const pageData = reactive({
      pageNum: 1,
      pageSize: 10,
      total: 0,
    });
		// 申请弹出显示开关
		const isShow = ref(false);
		// 申请请假表单
		const applyForm = reactive({
			applyType: 1,
			date: '',
			leaveTime: '',
      dept: '',
      reasons: ''
		});
    //表单验证规则
    const rules = reactive({
    });
    //操作类型
    const action = ref("create");
    // 查看休假详情显示开关
    const detailShow = ref(false)
    // 查看详情数据
    const detailData = reactive({})
    onMounted(() => {
      getLeaveListRequest();
    });
    //获取审批列表数据
    const getLeaveListRequest = async () => {
      const params = { ...queryForm, ...pageData };
      try {
        const res = await proxy.$api.getLeaveList(params);
        leaveData.value = res.list;
        pageData.total = res.page.total;
      } catch (error) {
        console.log(error);
      }
    };
    //查询事件
    const onSearchHandler = () => {
      getLeaveListRequest();
    };
    //重置事件
    const onResetHandler = (name) => {
      proxy.$refs[name].resetFields();
    };
    // 分页触发事件
    const handleCurrentChange = (current) => {
      pageData.pageNum = current;
      getLeaveListRequest();
    };
		// 申请请假按钮事假
		const applyHandler = () => {
			isShow.value = true
		};
		// 弹窗取消按钮事件
		const dialogCancelHandler = () => {
			isShow.value = false
		};
		// 弹窗确定按钮事件
		const dialogSubmitHandler = () => {
			proxy.$refs['applyRuleForm'].validate(async (valid) => {
				try {
          if (valid) {
            let params = {...applyForm, startTime: applyForm.date[0], endTime: applyForm.date[1]};
            params.action = action.value;
            await proxy.$api.postLeave_C(params);
            if(params.action === 'create'){
                proxy.$message.success('申请成功');
            }
            isShow.value = false;
            getLeaveListRequest();
          } else {
            proxy.$message.error('您填写的信息不符合规则，请重新输入');
            return false;
          }
        } catch (error) {
          proxy.$message.error(error.stack)
        }
			});
		};
		// 休假时间下拉选中事件
		const leaveTimeChange = (val) => {
			const [startTime, endTime] = [val[0], val[1]]
			applyForm.leaveTime = (endTime - startTime) / (24 * 60 * 60 * 1000) + 1
		};
    // 表格每行查看按钮事件
    const handleSee = (row) => {
      detailData.dataClass = {
        1: "静态数据",
        2: "动态数据"
      }[row.dataClass]
      detailData.dataItem = row.dataItem
      detailData.dataCategory = {
        1: "人脸",
        2: "卡口",
        3: "结构化",
        4: "社区"
      }[row.dataCategory]
      detailData.dataLevel =  {
        1: "一级",
        2: "二级",
        3: "三级",
        4: "四级"
      }[row.dataLevel]

      detailData.dept = row.dept
      detailData.person = row.person
      detailData.personPhone = row.personPhone
      detailData.applyTime = row.applyTime
      detailData.reasons = row.reasons
      detailData.applyStateName = {
        1: "待审批",
        2: "审批中",
        3: "审批拒绝",
        4: "审批通过",
        5: "作废"
      }[row.applyState]
      detailData.applyState = row.applyState
      detailData.curAuditUserName = row.curAuditUserName
      detailData.applyUser = row.applyUser.userName
      detailData._id = row._id
      detailShow.value = true
    };
    // 表格每行的作废按钮事件
    const handleDelete = (_id) => {
      let params = {_id, action: 'delete'};
      proxy.$api.postLeave_C(params).then(res => {
        proxy.$message.success('作废成功！');
        getLeaveListRequest();
      }).catch(error => {
        proxy.$message.error(error.stack)
      })
    }
    return {
      queryForm,
      leaveData,
      columList,
      pageData,
      rules,
      action,
			isShow, // 申请请假弹出显示开关
			applyForm, // 申请请假表单
      detailShow, // 查看详情显示开关
      detailData, // 查看详情数据
      getLeaveListRequest, // 获取审批列表数据
      onSearchHandler, // 搜索事件
      onResetHandler, // 重置事件
      handleCurrentChange, // 页码改变事件
			applyHandler, // 申请请假事件
			dialogCancelHandler, // 弹窗取消按钮事件
			dialogSubmitHandler, // 弹窗确定按钮事件
			leaveTimeChange, // 休假时间下拉选中事件
      handleSee, // 表格每行查看按钮事件
      handleDelete, // 表格每行的作废按钮事件
    };
  },
};
</script>
